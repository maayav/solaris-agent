#!/bin/bash
# Solaris Agent MVP - Smoke Test
# Verifies the graph backbone works end-to-end

set -e

echo "╔════════════════════════════════════════════════════════╗"
echo "║       Solaris Agent - Graph Backbone Smoke Test      ║"
echo "╚════════════════════════════════════════════════════════╝"
echo ""

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

# Track results
PASSED=0
FAILED=0

run_test() {
    local name="$1"
    local cmd="$2"
    
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    echo -e "${YELLOW}Testing: ${name}${NC}"
    echo "Command: $cmd"
    echo ""
    
    if eval "$cmd"; then
        echo -e "${GREEN}✓ PASSED${NC}"
        ((PASSED++))
    else
        echo -e "${RED}✗ FAILED${NC}"
        ((FAILED++))
    fi
    echo ""
}

# Change to agent-swarm directory
cd "$(dirname "$0")/agent-swarm" 2>/dev/null || cd agent-swarm

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "STEP 1: Infrastructure Check"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""

# Check if FalkorDB is reachable
echo "Checking FalkorDB connection..."
if redis-cli -h "${FALKORDB_HOST:-caboose.proxy.rlwy.net}" -p "${FALKORDB_PORT:-50353}" -a "${FALKORDB_PASSWORD:-uLkhZrFuAgKdopfJyxMFGoiVgpTStcRC}" ping 2>/dev/null | grep -q PONG; then
    echo -e "${GREEN}✓ FalkorDB is reachable${NC}"
    ((PASSED++))
else
    echo -e "${RED}✗ FalkorDB is NOT reachable${NC}"
    echo "  Will try running tests anyway..."
    ((FAILED++))
fi
echo ""

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "STEP 2: Unit Tests"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""

run_test "Schema Validation" "bun test tests/unit/schema.test.ts"
run_test "Event Bus Unit Tests" "bun test tests/unit/event-bus.test.ts"

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "STEP 3: Integration Tests"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""

run_test "Event Flow Integration" "bun test tests/integration/event-flow.test.ts"

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "STEP 4: Graph CRUD & Lifecycle Tests"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""

